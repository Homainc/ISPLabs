CREATE OR REPLACE FUNCTION UPDATE_PARTITION(
        P_ID IN NUMBER,
        NEW_NAME IN VARCHAR2,
        ER OUT VARCHAR2)
        RETURN BOOLEAN  
AS
BEGIN
    UPDATE FORUM_PARTITION
    SET NAME = NEW_NAME WHERE ID = P_ID;
    RETURN TRUE;
EXCEPTION
    WHEN OTHERS THEN
        ER:= 'Update failed for partition';
    RETURN FALSE;
END;