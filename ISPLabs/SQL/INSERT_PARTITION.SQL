CREATE OR REPLACE FUNCTION INSERT_PARTITION( 
    PARTITION_ID OUT NUMBER,
    P_NAME IN VARCHAR2,
    ER OUT VARCHAR2) 
RETURN NUMBER 
AS
BEGIN
    INSERT INTO FORUM_PARTITION(ID, NAME) VALUES(PARTITION_ID, P_NAME);
    SELECT ID INTO PARTITION_ID FROM FORUM_PARTITION WHERE NAME = P_NAME; RETURN 1;
EXCEPTION
    WHEN OTHERS THEN
        ER := 'Insert partition failed'; 
        RETURN 0; 
END;