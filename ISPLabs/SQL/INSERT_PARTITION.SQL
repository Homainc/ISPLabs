CREATE OR REPLACE FUNCTION INSERT_PARTITION(
    P_ID IN NUMBER,
    P_NAME IN VARCHAR2,
    ER OUT VARCHAR2)
    RETURN BOOLEAN
AS
BEGIN
    INSERT INTO FORUM_PARTITION(ID, NAME)
    VALUES(P_ID, P_NAME);
    RETURN TRUE;
EXCEPTION
    WHEN OTHERS THEN
        ER := 'Insert partition failed';
    RETURN FALSE;
END;
        