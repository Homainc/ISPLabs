CREATE OR REPLACE FUNCTION INSERT_TOPIC(
    P_ID IN NUMBER,
    P_NAME IN VARCHAR2,
    P_CATEGORY_ID IN NUMBER,
    P_USER_ID IN NUMBER,
    P_PUBLISH_DATE  IN DATE,
    P_IS_CLOSED IN NUMBER,
    ER OUT VARCHAR2)
    RETURN BOOLEAN
AS
BEGIN
    INSERT INTO TOPIC(ID, NAME, CATEGORY_ID, USER_ID, PUBLISH_DATE, IS_CLOSED)
    VALUES(P_ID, P_NAME, P_CATEGORY_ID, P_USER_ID, P_PUBLISH_DATE, P_IS_CLOSED);
    RETURN TRUE;
EXCEPTION
    WHEN OTHERS THEN
        ER := 'Insert topic failed';
    RETURN FALSE;
END;
        